#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		s;
	long	r;

	i = 0;
	s = 1;
	r = 0;
	while (('\t' <= str[i] && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while ('0' <= str[i] && str[i] <= '9')
	{
		r = (str[i] - 48) + (r * 10);
		i++;
		if ((r * s) < INT_MIN)
			return (0);
		else if ((r * s) > INT_MAX)
			return (-1);
	}
	return (r * s);
}
